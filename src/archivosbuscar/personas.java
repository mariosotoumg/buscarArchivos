    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String eol = System.getProperty("line.separator");
        try {
            BufferedWriter archivo  = new BufferedWriter(new FileWriter("listado.txt",true));
            String cadena = "";
            cadena += tNombre.getText()+"|";
            cadena += tApellido.getText()+"|";
            cadena += tCorreo.getText()+"|";
            cadena += tTelefono.getText()+eol;
            archivo.write(cadena);
            archivo.flush();
            archivo.close();
            tNombre.setText("");
            tApellido.setText("");
            tCorreo.setText("");
            tTelefono.setText("");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "No se pudo guardar el registro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String linea;
        String[] registro = new String[4];
        String buscar = tBuscar.getText();

        try{
            BufferedReader br = new BufferedReader(new FileReader("listado.txt"));
            while ( (linea = br.readLine() ) != null) {
                registro = linea.split("\\|");
                if(buscar.compareTo(registro[0]) == 0){
                    tNombre.setText(registro[0]);
                    tApellido.setText(registro[1]);
                    tCorreo.setText(registro[2]);
                    tTelefono.setText(registro[3]);
                }
            }

            br.close();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "No se puede encontrar el archivo");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "No se puede leer el archivo");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

